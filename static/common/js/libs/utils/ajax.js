function ajaxCore(url, async, type, data, contentType, context, callback){
	$.ajax({ 
		url: url, //"/docmaster"
		async: async, //true
		type: type, //"POST","GET"
		data: data,  //text, object
		contentType: contentType, //"application/x-www-form-urlencoded"		
		context: context, 	//this	
		cache: false,
		success: callback
	}); 
} 


